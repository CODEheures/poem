            <p>Personalized Open Education for the Masses (POEM) a pour but de faciliter l’acquisition de la
                connaissance par le peuple à travers des cursus créés sur-mesure et impliquants l’apprenant Personalized
                Open Education for the Masses (POEM) a pour but de faciliter l’acquisition de la connaissance par le
                peuple à travers des cursus créés sur-mesure et impliquants l’apprenant.
            </p>
        </div>
        <div class="right">
            <img src="./css/assets/prezi.png" alt="image prezi">
            <p><a href="#" title="présentation prezi">Voir le tutoriel sur prezi</a></p>
        </div>
        <div class="clearfix"></div>
    </div>
    <div class="second">
        <div class="left">
            <p>
                POEM permet d'évaluer de grands groupes d’apprenants à travers des questions riches et de façon
                automatique. Il offre à l'enseignant une interface simple et intuitive de constitution de cours (une
                playlist de leçons), soit en puisant dans le pool de leçons déjà disponibles soit par la mise en ligne
                de son propre contenu pédagogique.
            </p>
        </div>
        <div class="right">
            <p>
                POEM est basé sur un système de MOOC (Massively Open Online Courses), avec la plus-value qu'il utilise
                des algorithmes (basés sur l’optimisation par colonies de fourmis) pour choisir et proposer de manière
                individuelle des leçons adaptées aux objectifs, au niveau et aux compétences de l’étudiant.
            </p>
        </div>
        <div class="clearfix"></div>
    </div>
    <div class="separator">
        <img src="./css/assets/chemin.png" alt="separator">
    </div>
    <div class="third">
        <div class="left">
            <p>
                POEM s’adresse a toute personne souhaitant acquérir ou partager des connaissances que ce soit l’étudiant
                ou le professeur. Un étudiant peut être inscrit par un professeur (leçons obligatoires avec échéances),
                ou bien guidé par les leçons que POEM sélectionne pour lui dans le but d’atteindre l’objectif
                pédagogique qu’il s’est fixé. La plateforme étant ouverte, les curieux pourront naviguer parmi
                l’ensemble des leçons et y participer à leur guise.
            </p>
        </div>
        <div class="middle">
            <p>
                Toute leçon est constituée d’un support visuel et d’une évaluation pair-à-pair qui se fait par des
                questions ouvertes. L’étudiant est amené à répondre à une série de question, mais aussi à corriger et
                noter les questions et réponses de ses pairs, ainsi qu’à proposer de nouvelles questions lui-même. La
                note finale d’un étudiant dépend ainsi de sa compréhension du sujet (note pédagogique) mais aussi de sa
                participation (note participative).
            </p>
        </div>
        <div class="right">
            <p>
                Pour le professeur POEM offre un interface où il peut simplement et intuitivement apporter des leçons,
                constituer des cours, suivre la progression de ses étudiants ainsi qu’obtenir des données analytiques
                concernant ses cours ou leçons. In fine, il obtient des notes d’évaluation fiables (COPA) qu’il pourra
                vérifier, modifier et utiliser simplement. L’aspect ouvert de POEM permet au professeur de composer à
                partir de l’ensemble des leçons disponibles sur POEM.
            </p>
        </div>
        <div class="clearfix"></div>
    </div>
